namespace SymbolicExecution.AbstractSyntaxTree.Implementations;

public class BracketedArgumentListSyntaxAbstraction : BaseArgumentListSyntaxAbstraction
{
    public BracketedArgumentListSyntaxAbstraction(ImmutableArray<ISyntaxNodeAbstraction> children, ISymbol? symbol, Location location) : base(children, symbol, location)
    {
    }

    public override TaggedUnion<IEnumerable<IAnalysisState>, AnalysisFailure> AnalyzeNode(IAnalysisState previous)
    {
        return new AnalysisFailure("Bracketed argument lists are not supported", Location);
    }

    public override TaggedUnion<ImmutableArray<(IObjectInstance, IAnalysisState)>, AnalysisFailure> GetExpressionResults(IAnalysisState state)
    {
        return new AnalysisFailure("Cannot get the result of a bracketed argument list", Location);
    }
}